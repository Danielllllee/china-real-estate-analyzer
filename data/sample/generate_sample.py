def generate_all():
    """写入经过核实的区域均价、租金和宏观数据"""
    init_db()
    with get_connection() as conn:
        # 写入区域均价和租金数据
        for city, districts in VERIFIED_DISTRICT_PRICES.items():
            rents = VERIFIED_DISTRICT_RENTS.get(city, {})
            for district, price in districts.items():
                rent = rents.get(district)
                # 计算租售比: 月租金/房价
                rent_to_price = round(rent / price, 6) if rent else None
                conn.execute("""
                    INSERT OR REPLACE INTO district_stats
                    (city, district, month, avg_unit_price, avg_rent_per_sqm, rent_to_price_ratio)
                    VALUES (?, ?, '2026-03', ?, ?, ?)
                """, (city, district, price, rent, rent_to_price))

        # 写入宏观经济数据
        for city, macro in VERIFIED_MACRO_DATA.items():
            conn.execute("""
                INSERT OR REPLACE INTO macro_data
                (city, data_year, gdp, population, per_capita_income)
                VALUES (?, ?, ?, ?, ?)
            """, (city, macro["data_year"], macro["gdp"],
                  macro["population"], macro["per_capita_income"]))

    print("真实数据写入完成！共{}个城市，{}个区域".format(
        len(VERIFIED_DISTRICT_PRICES),
        sum(len(d) for d in VERIFIED_DISTRICT_PRICES.values())
    ))


if __name__ == "__main__":
    generate_all()
