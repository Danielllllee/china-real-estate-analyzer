def generate_all():
    """仅写入经过核实的区域均价数据"""
    init_db()
    with get_connection() as conn:
        for city, districts in VERIFIED_DISTRICT_PRICES.items():
            for district, price in districts.items():
                conn.execute("""
                    INSERT OR IGNORE INTO district_stats
                    (city, district, month, avg_unit_price)
                    VALUES (?, ?, '2026-03', ?)
                """, (city, district, price))

    print("真实数据写入完成！")


